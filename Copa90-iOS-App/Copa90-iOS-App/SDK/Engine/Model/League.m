#import "League.h"

@implementation League

- (nullable id)initWithModel:(nonnull NSMutableDictionary *)modelInstance
{
    self = [super init];
    if (self)
    {
        _identifier     = [modelInstance valueForKey:@"id"];
        _name           = [modelInstance valueForKey:@"name"];
    }
    return self;
}

@end
